// ── 상태 ──────────────────────────────────────
let selectedCategory = '전체';
let expandedId = null;

const CATEGORIES = ['전체', '시장 역사', '특산물', '여행 정보', '문화', '트렌드', '향토음식', '시장 소식', '계절 특집', '상인 이야기'];

// ── 렌더 ──────────────────────────────────────
function renderNews() {
  const container = document.getElementById('newsGrid');
  if (!container) return;

  const filtered = selectedCategory === '전체'
    ? NEWS_DATA
    : NEWS_DATA.filter(n => n.category === selectedCategory);

  container.innerHTML = filtered.map(article => `
    <div class="news-card ${expandedId === article.id ? 'expanded' : ''}" data-id="${article.id}">
      <div class="news-card-header" style="background:${article.color}">
        <span class="news-card-emoji">${article.emoji}</span>
        <span class="news-card-cat">${article.category}</span>
        <span class="news-card-date">${article.date}</span>
      </div>
      <div class="news-card-body">
        <h3 class="news-card-title">${article.title}</h3>
        <p class="news-card-summary">${article.summary}</p>
        ${expandedId === article.id ? `
          <div class="news-card-full">
            ${article.body.split('\n\n').map(p => `<p>${p}</p>`).join('')}
          </div>
        ` : ''}
        <button class="news-card-btn" onclick="toggleArticle(${article.id})">
          ${expandedId === article.id ? '접기 ↑' : '더 읽기 →'}
        </button>
        <div class="news-card-meta">
          <span>📖 읽는 시간 ${article.readTime}</span>
        </div>
      </div>
    </div>
  `).join('');
}

function renderCategoryTabs() {
  const tabs = document.getElementById('newsCategoryTabs');
  if (!tabs) return;

  tabs.innerHTML = CATEGORIES.map(cat => `
    <button
      class="news-cat-tab ${selectedCategory === cat ? 'active' : ''}"
      onclick="selectCategory('${cat}')"
    >${cat}</button>
  `).join('');
}

function selectCategory(cat) {
  selectedCategory = cat;
  expandedId = null;
  renderCategoryTabs();
  renderNews();
}

function toggleArticle(id) {
  expandedId = expandedId === id ? null : id;
  renderNews();
  if (expandedId) {
    setTimeout(() => {
      const el = document.querySelector(`.news-card[data-id="${id}"]`);
      if (el) el.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 50);
  }
}

function initNews() {
  renderCategoryTabs();
  renderNews();
}

document.addEventListener('DOMContentLoaded', initNews);
